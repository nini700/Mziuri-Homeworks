package savarjisho1;

public class Adamiani {
    private String saxeli;
    private String gvari;
    private int asaki;

    public Adamiani(String saxeli, String gvari, int asaki) {
        this.saxeli = saxeli;
        this.gvari = gvari;
        this.asaki = asaki;
    }

    public String getSaxeli() {
        return saxeli;
    }

    public void setSaxeli(String saxeli) {
        this.saxeli = saxeli;
    }

    public String getGvari() {
        return gvari;
    }

    public void setGvari(String gvari) {
        this.gvari = gvari;
    }

    public int getAsaki() {
        return asaki;
    }

    public void setAsaki(int asaki) {
        this.asaki = asaki;
    }
}